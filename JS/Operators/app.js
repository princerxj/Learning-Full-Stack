// Arithmetic Operators (+,-,*,**,/,%)
let a = 10;
let b = 5;
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);

//Unary Operators (++,--)
a = 10;
console.log(a++); //Post Increment
console.log(++a); //Pre Increment

//Assignment Oeprators (=,+=,*=,/=,%= etc.)
a = 10;
b = a;
console.log(b);

//Comparison Operators (>,>=,<,<=,!=,==, ===) gives true or false
let age = 18;
console.log(age>18);
